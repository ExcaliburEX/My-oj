/*
题目描述 Description
奶牛们又在玩一种无聊的数字游戏。输得很郁闷的贝茜想请你写个程序来帮她在开局时预测结果。在游戏的开始，每头牛都会得到一个数N(1<=N<=1,000,000)。此时奶牛们的分数均为0。如果N是奇数，那么奶牛就会把它乘以3后再加1。如果N是偶数，那么这个数就会被除以2。数字每变动一次，这头奶牛就得到1分。当N的值等于1时，游戏结束，此时的分数就是这头奶牛在这局游戏中的最终得分。 以下是N的初始值为5时，一局游戏的完整过程：

N 操作后所得数 注释 总分

5 16 3*5+1 1

16 8 16/2 2

8 4 8/2 3

4 2 4/2 4

2 1 2/2 5

这头奶牛的最终得分是5。

输入描述 Input Description
* 第1行: 一个正整数，N

输出描述 Output Description
* 第1行: 输出一个正整数N，即奶牛在这局游戏中的最终得分

样例输入 Sample Input
112

样例输出 Sample Output
20

数据范围及提示 Data Size & Hint
USACO2006十月月赛



*/




#include <stdio.h>
int main()
{
    int n,score=0;
	scanf("%d",&n);
	while(n!=1){
		if(n%2==0){
			n/=2;
			score+=1;
		}else{
			n=n*3+1;
			score+=1;
		}
		
	}
	printf("%d",score);
    return 0;
}
